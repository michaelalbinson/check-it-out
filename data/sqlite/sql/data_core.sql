PRAGMA foreign_keys = ON;

DROP TABLE IF EXISTS `PRESIDENT`;

CREATE TABLE `PRESIDENT`(
    `NUMBER` INT PRIMARY KEY,
    `NAME` VARCHAR(100) NOT NULL,
    `BIRTH_YEAR` INT NOT NULL,
    `DEATH_YEAR` INT DEFAULT NULL,
    `TOOK_OFFICE` INT NOT NULL,
    `LEFT_OFFICE` INT DEFAULT NULL,
    `PARTY` VARCHAR(100) DEFAULT NULL
);

DROP TABLE IF EXISTS `PRESIDENT_TO_SESSION`;

CREATE TABLE `PRESIDENT_TO_SESSION`(
    `SESSION_NUMBER` INT NOT NULL,
    `PRESIDENT_NUMBER` INT NOT NULL,
    FOREIGN KEY(SESSION_NUMBER) REFERENCES CONGRESSIONAL_SESSION(NUMBER),
    FOREIGN KEY(PRESIDENT_NUMBER) REFERENCES PRESIDENT(NUMBER),
    UNIQUE(PRESIDENT_NUMBER, SESSION_NUMBER)
);

DROP TABLE IF EXISTS `CONGRESSIONAL_SESSION`;

CREATE TABLE `CONGRESSIONAL_SESSION`(
    `NUMBER` INT PRIMARY KEY,
    `START_DATE` INT NOT NULL,
    `END_DATE` INT NOT NULL
);

DROP TABLE IF EXISTS `BILL_KEYWORDS`;

CREATE TABLE `BILL_KEYWORDS`(
    `KEYWORD` VARCHAR(100) NOT NULL,
    `BILL_ID` VARCHAR(100) NOT NULL,
    `WEIGHT` INT DEFAULT 0,
    UNIQUE(KEYWORD, BILL_ID),
    FOREIGN KEY(BILL_ID) REFERENCES BILL(ID)
);

DROP TABLE IF EXISTS `BILL`;

CREATE TABLE `BILL` (
    `ID` VARCHAR(100) PRIMARY KEY,
    `TITLE` VARCHAR(500) DEFAULT NULL,
    `SHORT_TITLE` VARCHAR(500) DEFAULT NULL,
    `CONGRESS_SESSION` INT DEFAULT 0,
    `INTRODUCED_DATE` INT DEFAULT 0,
    `BILL_URL` VARCHAR(100) DEFAULT NULL,
    `ACTIVE` BOOLEAN DEFAULT FALSE,
    `ENACTED` BOOLEAN DEFAULT FALSE,
    `VETOED` BOOLEAN DEFAULT FALSE,
    `SUMMARY` VARCHAR(500) DEFAULT NULL,
    `LATEST_MAJOR_ACTION` VARCHAR(500) DEFAULT NULL,
    FOREIGN KEY(CONGRESS_SESSION) REFERENCES CONGRESSIONAL_SESSION(NUMBER)
);

